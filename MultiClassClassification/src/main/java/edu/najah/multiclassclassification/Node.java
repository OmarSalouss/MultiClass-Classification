package edu.najah.multiclassclassification;

/**
 *
 * @author YousefHanbali & OmarSalous
 */
public class Node {
    
    float x;
    float y;
    String label;

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }

    public String getLabel() {
        return label;
    }

    public void setLabel(String label) {
        this.label = label;
    }
}
